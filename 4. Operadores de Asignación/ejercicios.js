// Ejercicio 4.1: Declara una variable 'x' y asígnale el valor 10. Usa los operadores de asignación compuesta para realizar las siguientes operaciones:
// - Suma 5 a 'x'.
// - Resta 3 de 'x'.
// - Multiplica 'x' por 2.
// - Divide 'x' entre 4.
// - Calcula el módulo de 'x' con 3.

// Declarar la variable 'x' y asignarle el valor 10
let x = 10;
x += 5;   // Suma 5 a 'x'
x -= 3;   // Resta 3 de 'x'
x *= 2;   // Multiplica 'x' por 2
x /= 4;   // Divide 'x' entre 4
x %= 3;   // Calcula el módulo de 'x' con 3


